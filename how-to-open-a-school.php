					<h3>About CBSE</h3>
					<p>The Central Board of Secondary Education (CBSE) is an International level board of education in India, which acts as an examining body for secondary and senior secondary education in various government and private schools.</p>
					<p>The board comes under Ministry of HRD, under the Central Government of India and has its headquarters in Preet Vihar, New Delhi.</p>
					<h3>History of CBSE</h3>
					<p>The first education board to be set up in India in 1921. It was the “Uttar Pradesh Board of High School and Intermediate Education”. This board was formed under the jurisdiction of Rajputana, Central India and Gwalior. In 1929, the government of India set up a joint board named "Board of High School and Intermediate Education, Rajputana". This included Ajmer, Merwara, Central India and Gwalior. Later it was confuted to Ajmer, Bhopal. In 1952, it became the "Central Board of Secondary Education".</p>
					<h3>The Present Scenario</h3>
					<p>As on date, there are about 21,000 schools in India and 250 schools in 28 foreign countries affiliated to the CBSE. The board has 10 Regional Offices, spread across the country, which are responsible for conducting AISSE (Class X exams) and AISSCE (Class XII exams). The board has a very high reputation not only within India, but even outside India.</p>
					<h3>How to Start a CBSE School</h3>
					<p>To start a school, the first requirement is to form a society because according to norms made by government, a school can only be run by a society. Once the society is formed, the foremost requirement is of the land on which school will be established. Depending upon the geographic location of the school, and number of sections desired, CBSE has detailed out the minimum size of land. After, this, the next step is to get affiliation from state board of the respective state. Once this affiliations is received, to get CBSE affiliation, we need a No Objection Certificate (NOC)  from the state board.</p>
					<p>We need to decide the curriculum for the school as per the guideline given by the board. The next step is to identify and hire teaching and non-teaching staff as per the CBSE norms. The teaching staff has to undergo compulsory training by the CBSE, the expenses for which are borne by the school. The board, first gives affiliation up to secondary level (Class 10). After 2 years, you may apply for affiliation up to Senior Secondary level (Class XII).</p>
