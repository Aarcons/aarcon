					<h3>Is Affiliation of a school mandatory? </h3>
					<p>As per Section 18 of the Right of Children to Free and Compulsory Education Act, 2009, “No school, can be established or allowed to function without obtaining a certificate of recognition from such authority”.  Hence, it is mandatory for every school to take affiliation from a State Board or Central Board Secondary Education.</p>
					<p>Any person who continues to run a school without this affiliation is liable to fine which may extend to Rs. 1,00,000 (Rupees One Lakh) in case of continuing contraventions, the person may be subjected to a fine of Rs. 10,000 per day till the time affiliation is not received. </p>
					<h3>What is the validity of Affiliation:</h3>
					<p>Initially, the board gives affiliation for a period of 3 years. This affiliation may be extended for 5 years, if the Board is satisfied with the functioning of the school and is subjected to school fulfilling all the norms prescribed by it.</p>
					<p>The school is granted a further extension for a period of 5 years, and after which the school has to renew the affiliation every 5 years.</p>
					<p>The entire application happens ONLINE only. The school has to upload all mandatory documents on the board portal. No hard copy/ submission through E-Mail is accepted /entertained by the board. Any document received in hard copy is out rightly rejected, without giving any reason. </p>
					<p>All the correspondence from the Board is sent through online link only and no hard copy is sent to the school.</p>
