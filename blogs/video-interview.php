                        <p>With remote working being the call of the hour, all job interviews have also taken the remote way.Although not an alien concept, video interviews have seldom been used on regular days.Do you have a video interview lined up? Then this is just the piece for you.Here are a few tips that can help you leave a lasting impression in your upcoming video interview.</p>
                        <h4>Practice Makes a man perfect</h4>
                        <p>A video interview is as important as a face to face interview.Even in a video interview, your recruiter will remember you for your confidence and how you tackle it.Therefore, you must practice until you exude confidence across the screen.Take the help of your family or friend in your practice sessions.This always helps to build confidence and getting things in perspective.</p>
                        <h4>Dress Appropriately</h4>
                        <p>Okay, now just because you don’t have to go out for the interview doesn’t mean that you can appear in your PJs.You must dress appropriately. If not completely, at least put on a freshly washed and ironed formal shirt.Just because you are stuck in the house doesn’t mean you can put forward an excuse of turning up for an interview like a caveman.Make sure to be neat and tidy and well-groomed. A lot is perceived from the way a person carries him/herself.</p>
                        <h4>Organize your things properly</h4>
                        <p>Make sure that your surrounding is clean and organized wherever you choose to sit down for the interview.Make sure that the camera whether inbuilt or webcam is well-positioned.In case you feel that it is not comfortable you can try to sit on a higher chair or place your laptop on a slightly alleviated platform like a thick textbook.Keep a hard copy of your resume handy, a pen (keeping a spare won’t hurt), and a notebook for taking notes.</p>
                        <h4>Put down some ground rules</h4>
                        <p>Make sure that your family or friends do not interrupt you while you are in your interview.Best, choose the room farthest from the main action in the house and is the most peaceful.Consider keeping your mobile on DND mode and away from your interview station.This will help you remain focused and go through it peacefully.</p>
                        <h4>Ensure that everything is functioning peacefully</h4>
                        <p>Since it’s a video interview, make sure that your gadgets are functioning properly.Check that your wifi is working well because it is extremely crucial.However, keep an extra dongle near, just to be on the safe side.Make trial calls and check everything to avoid extra tension on the day of the interview.</p>
                        <h4>Make best use of your advantage</h4>
                        <p>Do you know what adds up to your discomfort when you have to appear for a face-to-face interview? The thought of reaching a completely unknown place and its strange ambiance. One of the things that’s to your advantage over here is that you will be in the comfort of your home. Adding to this, reflect on your strengths and play to them accordingly.</p>
                        <h4>Acknowledge Everyone</h4>
                        <p>Be it a one on one video interview or a panel interview, it is important and basic etiquette to greet everyone.Even while answering try to give equal attention to everyone on the panel.Also, look directly into the camera otherwise it may seem that you are looking at something else on your screen.Apart from these, remember to sit upright and hold a good posture, take notes, and also research about the company and note down some questions for your recruiter.This will help them realize that you are taking your interview seriously.So, make sure to follow the above-mentioned points and you will be good to go. All the best!</p>
