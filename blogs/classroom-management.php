                        <h4><b>Techniques and Strategies for Classroom Management:</b></h4>
                        <ul>
                            <li><b>Establish a positive relationship with class:</b> The first step to successfully manage the classroom is to establish a positive relationship with the students. If some students misbehave then isolate those students from the rest of the class. And maintain a positive relationship with the whole class.</li>
                            <li><b>Eye the disturbance:</b> Make eye contact with the students who are misbehaving, or not paying attention. This will let the students know that they are off task and they should pay attention to the lesson. And by making gestures or eyeing the disturbance you will not disturb the whole class and nuisance has been taken care of.</li>
                            <li><b>Tight Transitions:</b> It is a technique that is practiced by experienced teachers. For example- if the teacher says ‘reading time’ then the students should stop whatever they are doing and start reading books. By utilizing this technique the teacher saves his and students time in transitioning from one activity to another.</li>
                            <li><b>Praise what and when you can:</b>  By practicing this technique you can make a good relation with your students. This involves calling attention to the things your students are doing that meet your expectations. It enables you to restate and reinforce the expectations for student behavior in a non-negative way. By narrating on-task behavior, you enable students who may have misheard you the first time to hear exactly what you expect of them. It’s easier for students to meet your expectations when it’s amply clear what you expect of them.</li>
                            <li><b>And Lastly love your students:</b> Love your students and stand against the behavior that doesn’t meet your expectations. By loving our students unconditionally we remind them of their true worth. The students should know how the teachers feel about them. The students should know that you believe in them so, they will not do anything that you don’t expect them to do.</li>
                        </ul>
