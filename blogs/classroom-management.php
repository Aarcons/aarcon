                        <p>If you want to become a good teacher then you should know the <b>student management techniques</b> as well as <b>classroom management techniques</b>. To do successful classroom management one should take care of many things that he/she has to do. </p>
                        <p>Some of the most important things are, classroom activities, student behaviors, student performance, keeping students organized, orderly, focused, attentive, on task, and academically productive</p>
                        <p>There is a saying that “effective teachers tend to display strong classroom management skills”. Thus your presence and the response of the students can easily tell how good your classroom management skills are. Some of the tools which you can use to do effective classroom management are listed below:</p>
