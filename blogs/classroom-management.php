                        <h4><b>3. Learn Boost:</b></h4>
                        <p>This app allows a teacher to record and organize a student’s progress. It can also be integrated with google apps, which makes it a convenient time saver. It also allows the teacher to share student’s progress with his/her parents and even allows the teacher to design a lesson plan according to the progress report.</p>
