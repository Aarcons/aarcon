                        <p>Remember the times when we complained about not having a summer vacation just because we have become working professionals? But now we are just sulking because the horrid COVID-19 has got us all under house arrest. With nowhere to go, no more Saturday night outings, it can get very boring and depressing for many. Worry no more! With these simple tips, you can stay positive through these tough times with ease.</p>
                        <h4>Follow a routine</h4>
                        <p>As children, our parents used to put up a daily routine in front of our study table for us to manage the day better. Why should it be any different when we’re grown up? Following a routine has become all the more important for us when we’re quarantined for almost a month and a half now. With Microsoft To Do, MeisterTask , etc, this habit can easily be inculcated. Following this practice can not only help you to be more productive but also helps you stay positive and focused.</p>
                        <h4>Get some exercise</h4>
                        <p>With all the fitness centers shut for safety measures, our fitness regime has taken a back seat. However, WHO has provided a statement that it is all the more important for us to exercise. Why? Simple. By exercising, our body releases hormones called endorphins. These endorphins help us to stay happy and positive and keeps all the negative energy at bay. Apps like Healthifyme , cure.fit etc helps you to workout at home and stay motivated during these gloomy quarantine days.</p>
                        <h4>Follow up on your long lost hobby</h4>
                        <p>We all have had our hobbies and been extremely passionate about it. But somewhere between adulting and balancing work and personal life, it has been long lost. Well, why not utilize our quarantine time to brush up on our hobbies once again? After all, such a time is indeed a once in a lifetime opportunity. This will also help you to relax after the day’s work and also help in the creation of happy hormones.</p>
                        <h4>Avoid being addicted to news</h4>
                        <p>Now, it is understandable how important news is during such crucial times. Also, the media has to put forward the news as it is, without any sugarcoating. So, it is best to avoid hogging onto it given that we humans tend to panic at the slightest of inconvenience. It is alright to stay abreast of the current scenario but not let it negatively affect you.</p>
                        <h4>Stay Connected</h4>
                        <p>We have always cribbed how our busy schedule leaves us with little or no time to stay connected with our loved ones. Well, looks like the universe has decided to grant our wish (even in the unlikeliest of scenario). Find a time during the day to pick up your phone and call up your family and friends. Trust me, there can be no better feeling than ringing up your close ones, thereby beating the blues with love.</p>
                        <h4>Spend time with family</h4>
                        <p>Can you remember the last time you had so much time to spend with your family? Only today, a friend of mine had updated a status which said, “After school, this is the first time I get to enjoy my summer vacation!” So, don’t let the opportunity slide away. Spend as much time with your family and loved ones as possible. This will not only make them happy but will keep all the negative thoughts far away.</p>
                        <h4>Reflect on your goals and work towards it</h4>
                        <p>Often during our usual hustle days, we are left with hardly any energy to reflect on our goals and how far we have achieved it. But now with everything at a standstill grab the opportunity and make the best use of it! Utilize your lockdown to the optimum by working towards your goals. Add up to your skillset, gather more knowledge that will help to accelerate your career. Hopefully, with these tips and tricks, you will be able to keep all the negativity far away. Also, stay hydrated, stay indoors and follow the government's guidelines, and everything will be fine.</p>
