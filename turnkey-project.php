					<h3>What is meant by Turnkey Project?</h3>
					<p>Typically, when a person, starts a new business, there are 2 broad things to be done. The first is designing the project and the other is building that project. Now, a person can hire 2 separate people to do these jobs, or may hire one person to do the entire job. This is what is called a turnkey project.</p>
					<p>A turnkey project is a very special mode of starting a new project. It is actually a contract, under which a firm undertakes the responsibility to design, build and equip a business or service facility and then hand over the keys to the purchaser when it is ready for operation.</p>
					<h3>What are the benefits of a turnkey project?</h3>
					<ul>
						<li>Reduced time for the design and construction process.</li>
						<li>Cost benefit, as all the cost elements come under one service provider.</li>
						<li>Since the designing and construction is done by same person, so there are no chances of any confusion.</li>
						<li>Any changes to be done, while constructing it are easily managed, as the same person is handling both things.</li>
						<li>Most suitable for low cost projects, which have a limited budget.</li>
						<li>You don't have to interact with multiple people, as the entire work is done by one person. So, basically, you get 'peace of mind'.</li>
