					<h3>What is Detailed Project Report?</h3>
					<p>The Detailed Project Report (or commonly called DPR) is the most important document required to start a new business. And, when someone is planning to start a new educational institute, this is the main document helps the affiliating body decide on whether or not to give affiliation.</p>
					<p>The DPR is a complete docket which contains all information related to the project. This serves two main purposes â€“ while for the affiliating body it helps to understand the background of the promoters, and various details related to the project like ownership of the land, infrastructure, availability of required approval from various government bodies etc. This then becomes the base for taking a decision whether or not to give the approval. On the other hand, for the promoters, it helps as the main tool for making the decision to make the required investment and accordingly do the planning.</p>
					<p>A detailed project report covers all the aspects of institute, from analysing the market, confirming availability of infrastructure such as building, laboratories, sports ground, various equipments among other things. The scope of the DPR includes general information about the institute, promoters behind the institute, their vision, the economic and financial feasibility of the institute. It gives information about the people behind the institute, their vision, cost of the project, fund requirement, source of funding, and returns from the project, cash flow estimates and its profitability.</p>
					<p>DPR gives an idea about the time of completion, funds requirement for the project, its sources, its uses, working capital requirement and activities required to undertake the project.On this basis, the client can evaluate the growth period and can control the quality and quantity of the project as per the desired level.</p>
